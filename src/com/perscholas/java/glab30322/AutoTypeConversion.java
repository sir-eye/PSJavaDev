package com.perscholas.java.glab30322;

public class AutoTypeConversion {
    public static void main(String[] args) {

        System.out.println("****************************************************");
        System.out.println("\n");
        System.out.println("Auto Type Conversion Demo\n");
        int x = 20;
        double y = 40.5;
        long p = 30;
        float q = 10.60f;
        double z = x + y;
        System.out.println("Sum of two numbers: " + z);

        float r = p - q;
        System.out.println("Subtraction of two numbers: " + r);
        System.out.println("\n");
        System.out.println("****************************************************");
    }
}
