package com.perscholas.java.glab30321;

public class LogicalOperatorsDemo {
    public static void runTask() {

        boolean x = true;
        boolean y = false;

        System.out.println("x & y : " + (x & y));
        System.out.println("x && y : " + (x && y));
        System.out.println("x | y : " + (x | y));
        System.out.println("x || y: " + (x || y));
        System.out.println("x ^ y : " + (x ^ y));
        System.out.println("!x : " + (!x));
    }
}

